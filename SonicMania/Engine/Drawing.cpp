bool InitRenderDevice() {

    for (int s = 0; s < SCREEN_MAX; ++s) {
        screens[s].frameBuffer = new ushort[SCREEN_XSIZE * SCREEN_YSIZE];
        memset(screens[s].frameBuffer, 0, (SCREEN_XSIZE * SCREEN_YSIZE) * sizeof(ushort));

        screens[s].width         = SCREEN_XSIZE;
        screens[s].height        = SCREEN_YSIZE;
        screens[s].screenCenterX = SCREEN_XSIZE / 2;
        screens[s].screenCenterY = SCREEN_YSIZE / 2;
        screens[s].clipBound_X1  = 0;
        screens[s].clipBound_X1  = 0;
        screens[s].clipBound_X2  = SCREEN_XSIZE;
        screens[s].clipBound_Y2  = SCREEN_YSIZE;
    }

#if RETRO_USING_SDL2
    SDL_Init(SDL_INIT_EVERYTHING);

    SDL_DisableScreenSaver();

    SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "nearest");
    SDL_SetHint(SDL_HINT_RENDER_VSYNC, engine.vsync ? "1" : "0");

    engine.window = SDL_CreateWindow(engine.gameName, SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED, SCREEN_XSIZE * engine.windowScale,
                                     SCREEN_YSIZE * engine.windowScale, SDL_WINDOW_ALLOW_HIGHDPI);

    engine.renderer = SDL_CreateRenderer(engine.window, -1, SDL_RENDERER_ACCELERATED);

    if (!engine.window) {
        printLog("ERROR: failed to create window!");
        return 0;
    }

    if (!engine.renderer) {
        printLog("ERROR: failed to create renderer!");
        return 0;
    }

    SDL_RenderSetLogicalSize(engine.renderer, SCREEN_XSIZE, SCREEN_YSIZE);
    SDL_SetRenderDrawBlendMode(engine.renderer, SDL_BLENDMODE_BLEND);

    for (int s = 0; s < SCREEN_MAX; ++s) {
        engine.screenBuffer[s] =
            SDL_CreateTexture(engine.renderer, SDL_PIXELFORMAT_RGB565, SDL_TEXTUREACCESS_STREAMING, screens[s].width, screens[s].height);

        if (!engine.screenBuffer[s]) {
            printLog("ERROR: failed to create screen buffer!\nerror msg: %s", SDL_GetError());
            return 0;
        }
    }

    if (engine.startFullScreen) {
        SDL_RestoreWindow(engine.window);
        SDL_SetWindowFullscreen(engine.window, SDL_WINDOW_FULLSCREEN_DESKTOP);
        SDL_ShowCursor(SDL_FALSE);
        engine.isFullScreen = true;
    }

    if (engine.borderless) {
        SDL_RestoreWindow(engine.window);
        SDL_SetWindowBordered(engine.window, SDL_FALSE);
    }

    SDL_DisplayMode disp;
    if (SDL_GetDisplayMode(0, 0, &disp) == 0) {
        //engine.screenRefreshRate = disp.refresh_rate;
    }
    return true;
#endif
    return false;
}

