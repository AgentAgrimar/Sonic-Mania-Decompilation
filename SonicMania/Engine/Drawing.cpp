            destScreenPos[0].h = SCREEN_YSIZE / 2;

            destScreenPos[1].x = (SCREEN_XSIZE / 4);
            destScreenPos[1].y = SCREEN_YSIZE / 2;
            destScreenPos[1].w = SCREEN_XSIZE / 2;
            destScreenPos[1].h = SCREEN_YSIZE / 2;
            break;
